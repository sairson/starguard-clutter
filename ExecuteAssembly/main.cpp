HANDLE RedirectStdoutAndStderr() {
    HANDLE hReadPipe = NULL; // 读取管道的句柄
    HANDLE hResultPipe = NULL; // 管道结果的句柄
    // 管道的安全描述符
    STARTUPINFOA si;
    PROCESS_INFORMATION pi;
    SECURITY_ATTRIBUTES sa;

    ZeroMemory(&si, sizeof(si));
    ZeroMemory(&pi, sizeof(pi));
    ZeroMemory(&sa, sizeof(sa));
    si.cb = sizeof(si);

 

    sa.nLength = sizeof(SECURITY_ATTRIBUTES);
    sa.bInheritHandle = TRUE;
    sa.lpSecurityDescriptor = NULL;

    if (!CreatePipe(&hReadPipe, &hResultPipe, &sa, 0x200000))
    {
        printf("[-] CreatePipe Error %d\n", GetLastError());
        return NULL;
    }
    if (!SetStdHandle(STD_OUTPUT_HANDLE, hResultPipe)) {
        printf("[-] SetStdHandle STD_OUTPUT_HANDLE has an error %d\n", GetLastError());
        return NULL;
    }
    if (!SetStdHandle(STD_ERROR_HANDLE, hResultPipe)) {
        printf("[-] SetStdHandle STD_ERROR_HANDLE has an error %d\n", GetLastError());
        return NULL;
    }
    return hReadPipe;
}


